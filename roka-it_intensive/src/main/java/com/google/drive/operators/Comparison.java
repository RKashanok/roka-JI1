package com.google.drive.operators;

public class Comparison {

    public static void main(String[] args) {

        // > < >= <= == !=

        int a = 10;
        double b = 6.0;
        int c = 6;

        System.out.println(a > b);
        System.out.println(c >= b);
        System.out.println(c == b);
        System.out.println(c != b);

    }

}
